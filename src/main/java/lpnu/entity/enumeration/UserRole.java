package lpnu.entity.enumeration;

public enum UserRole {
    ADMIN, MANAGER, CUSTOMER


    /*
        ADMIN - має право створювати нові товари та переглядати все що є неактивним
        MANAGER - має право закрити замовлення, має право приймати товар на склад
        CUSTOMER - має право скасувати ззамовлення
     */
}
